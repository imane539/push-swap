#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*p;
	int		size;
	int		i;

	i = 0;
	size = (int)ft_strlen(s1);
	p = malloc(size + 1);
	if (!p)
		return (NULL);
	while (s1[i])
	{
		p[i] = s1[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
